package ReportsModule.BeanPakage;

public class ProductWiseTableData {
    String partyName;
    String represents;
    String city;
    String itemName;
    String itemSubgroup;
    String itemGroup;
    String quantity;
    String invAmount;
    public ProductWiseTableData() {
        super();
    }

    public void setPartyName(String partyName) {
        this.partyName = partyName;
    }

    public String getPartyName() {
        return partyName;
    }

    public void setRepresents(String represents) {
        this.represents = represents;
    }

    public String getRepresents() {
        return represents;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCity() {
        return city;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemSubgroup(String itemSubgroup) {
        this.itemSubgroup = itemSubgroup;
    }

    public String getItemSubgroup() {
        return itemSubgroup;
    }

    public void setItemGroup(String itemGroup) {
        this.itemGroup = itemGroup;
    }

    public String getItemGroup() {
        return itemGroup;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setInvAmount(String invAmount) {
        this.invAmount = invAmount;
    }

    public String getInvAmount() {
        return invAmount;
    }
}
